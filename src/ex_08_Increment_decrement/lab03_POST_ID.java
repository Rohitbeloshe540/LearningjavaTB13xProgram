package ex_08_Increment_decrement;

public class lab03_POST_ID {

    public static void main(String[] args) {


        // pre increment
        int a = 10;
        System.out.println(++a);
        System.out.println(a);


// expression and result table
//  line no  / a / result b
//    7      / 10 / NA
//    8      / 11/ 11


   // post increment

        int a_post = 10;
        System.out.println(a_post++);
        System.out.println(a_post);

        }
                }


